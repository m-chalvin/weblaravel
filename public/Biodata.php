            <th colspan="3">
                <p>Hai... Saya Muhammad Chalvin Syahputra, biasa di panggil Chalvin. Saya lahir dari keluarga sederhana yang bahagia, saya anak paling kecil di keluarga saya. Saya memiliki Hobi Traveling dan juga Hiking, semua hobi itu berawal ketika abang saya mengajak saya untuk mendaki gunung, dan gunung pertama kali yang saya daki adalah Bukit Gundul. Tepat pada hari Kemerdekaan Republik Indonesia pada 17 Agustus 2015 dan pada hari itu juga pertama kali saya memulai pendakian, awalnya saya cuma penasaran dan hanya ingin tau bagaimana rasanya mendaki tetapi setelah hari itu saya mulai terobsesi pada dunia pendakian. Menurut saya mendaki gunung adalah hal yang melelahkan, tetapi dari itu saya dapat belajar banyak hal, baik itu tentang persahabatan,keperdulian, menciptakan rasa kekeluargaan yang tinngi, dan yang paling penting mengajari saya tentang edukasi di alam bebas.</p>
